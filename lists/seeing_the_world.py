# Seeing the world

locations = ['maldives', 'bali', 'zanzibar', 'thailand', 'kigali']
print('Original list:', locations)
print('Sorted list:', sorted(locations))
print('Original list after sorted:', locations)
print('Reversed list:', sorted(locations, reverse=True))
print('list after sorted with reverse:', locations)
locations.reverse()
print('List after reverse:', locations)
locations.sort()
print('list after sort:', locations)
locations.sort(reverse=True)
print('List after sort in reverse:', locations)